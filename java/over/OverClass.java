package over;


/**
 * Класс, где представлены основные математические операции
 */
public class OverClass {
    /**
     * Метод суммировани двух чисел
     * @param a первое слагаемое
     * @param b второе слагаемое
     */
    public static double sum (double a, double b) {
        return a + b;
    }
    /**
     * Метод разности двух чисел
     * @param a уменьшаемое
     * @param b вычитаемое
     */
    public static double dif (double a, double b) {
        return a - b;
    }
    /**
     * Метод умножения двух чисел
     * @param a первый множитель
     * @param b второе множитель
     */
    public static double mult (double a, double b) {
        return a * b;
    }
    /**
     * Метод деления одного числа на другое
     * @param a частное
     * @param b делитель
     */
    public static double div (double a, double b) {
        return a / b;
    }
}
